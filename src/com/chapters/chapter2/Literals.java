package com.chapters.chapter2;

public class Literals {
  public static void main(String[] args) {
    int money = 71_400;

    System.out.println(money);

    int hex = 0xFF;
    System.out.println(hex);

    int octal = 0213;
    System.out.println(octal);

    int bin = 0b10;
    System.out.println(bin);
  }
}
