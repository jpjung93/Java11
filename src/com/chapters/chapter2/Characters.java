package com.chapters.chapter2;

public class Characters {
  public static void main(String[] args) {
    char c = 'X';

    System.out.println("this is the character: " + c);

    c++;
    System.out.println("This is the character incremented: " + c);

    c = 90;
    System.out.println("Set the character to 90: " + c);
  }
}
